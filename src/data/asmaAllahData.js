export const getRandomAsmaAllah = () => {
  const randomIndex = Math.floor(Math.random() * asmaAllahData.length);
  return asmaAllahData[randomIndex];
};

export const getAsmaAllahOfTheDay = () => {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  const index = dayOfYear % asmaAllahData.length;
  return asmaAllahData[index];
}; 